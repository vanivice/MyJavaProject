package lab2;

    public class Calculator {
        public static void add(int a, int b) {
            System.out.println(a + b);
        }

        public static void add(double a, double b) {
            System.out.println(a + b);
        }

        public static void add(long a, long b) {
            System.out.println(a + b);
        }

        public static void div(int a, int b) {
            System.out.println((double) a / b);
        }

        public static void div(double a, double b) {
            System.out.println(a / b);
        }

        public static void div(long a, long b) {
            System.out.println((double) a / b);
        }

        public static void mult(int a, int b) {
            System.out.println(a * b);
        }

        public static void mult(double a, double b) {
            System.out.println(a * b);
        }

        public static void mult(long a, long b) {
            System.out.println(a * b);
        }

        public static void subt(int a, int b) {
            System.out.println(a - b);
        }

        public static void subt(double a, double b) {
            System.out.println(a - b);
        }

        public static void subt(long a, long b) {
            System.out.println(a - b);
        }
    }